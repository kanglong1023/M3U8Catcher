package io.github.kanglong1023.m3u8.core;

public class M3u8Exception extends RuntimeException {

    public M3u8Exception() {
        super();
    }

    public M3u8Exception(String message) {
        super(message);
    }

    public M3u8Exception(String message, Throwable cause) {
        super(message, cause);
    }

}
